"""
message.py — Structured validation message for KiteML validation checks.
"""

from dataclasses import dataclass, field
from typing import Any

from kiteml.validation.severity import ValidationSeverity


@dataclass
class ValidationMessage:
    """
    A single structured message generated by a validation rule or check.

    Attributes
    ----------
    severity : ValidationSeverity
        The severity of the issue (INFO, WARNING, ERROR, CRITICAL).
    title : str
        Short summary title of the issue (e.g., 'Target Column Missing').
    description : str
        Detailed explanation of what was detected.
    suggestion : str | None
        Actionable advice or recommendation to fix the issue.
    rule_id : str | None
        Identifier of the rule that produced this message (e.g., 'rule_empty_dataset').
    code : str | None
        Standardized error or warning code (e.g., 'KML1002').
    context : dict[str, Any]
        Additional context data (e.g., column names, row counts, ratios).
    """

    severity: ValidationSeverity
    title: str
    description: str
    suggestion: str | None = None
    rule_id: str | None = None
    code: str | None = None
    context: dict[str, Any] = field(default_factory=dict)

    def to_dict(self) -> dict[str, Any]:
        """Convert message to a dictionary representation."""
        return {
            "severity": self.severity.value,
            "title": self.title,
            "description": self.description,
            "suggestion": self.suggestion,
            "rule_id": self.rule_id,
            "code": self.code,
            "context": self.context,
        }
